// תיקון כיווניות למספרים/מייל/שעות המשובצים בטקסט עברי ב-PDF.
//
// pdf-lib עם הפונט העברי מצייר טקסט עברי נכון, אך מספר/מייל *בתוך שורת עברית* מצויר RTL
// (הפוך). הפתרון שהוכח ואושר: להפוך ידנית את תווי כל טוקן LTR — pdf-lib מצייר את השורה
// RTL, וההיפוך המקדים גורם למספר להופיע נכון. חשוב: המספר חייב להיות באותה מחרוזת עם
// טקסט עברי (rightText אחד); שדה מספרי המצויר לבדו לא יתוקן ולכן יש לשלבו במחרוזת עברית.
// (LRO/POP לא כובדו ע"י הרנדרר; getReorderedString של bidi-js הפך גם את העברית.)

// טוקן LTR = מייל, או רצף ספרות/מפרידים (כולל טווח "10:30 - 13:00" ומספר "03-309-6655").
const LTR_TOKEN = /[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}|\d[\d.,:/]*(?:\s*[-–]\s*\d[\d.,:/]*)*/g

export function toVisual(text: string): string {
  return String(text ?? '').replace(LTR_TOKEN, m => [...m].reverse().join(''))
}
