// תיקון כיווניות למספרים/מייל/שעות המשובצים בטקסט עברי ב-PDF.
//
// pdf-lib עם הפונט העברי מצייר טקסט עברי טהור נכון, אך מצייר רצפי ספרות/לטינית בכיוון
// RTL — הפוך (12/07/2026 → 6202/70/21). LRO/POP אינם מכובדים ע"י הרנדרר, ו-bidi-js המלא
// הופך גם את העברית. הפתרון שהוכח בבדיקה (scratch_approaches): *להפוך ידנית* את תווי כל
// טוקן LTR (ספרות, מייל, טווח שעות) — כך שכאשר הרנדרר יצייר אותו RTL, הוא יֵראה נכון.

// טוקן LTR = כתובת מייל, או רצף ספרות/מפרידים (כולל טווח "10:30 - 13:00" ומספר "03-309-6655").
const LTR_TOKEN = /[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}|\d[\d.,:/]*(?:\s*[-–]\s*\d[\d.,:/]*)*/g

export function toVisual(text: string): string {
  return String(text ?? '').replace(LTR_TOKEN, m => [...m].reverse().join(''))
}
