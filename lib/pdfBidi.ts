// תיקון כיווניות למספרים/מייל/שעות/סוגריים המשובצים בטקסט עברי ב-PDF.
//
// pdf-lib עם הפונט העברי מצייר טקסט עברי נכון, אך:
//  1. מספר/מייל *בתוך שורת עברית* מצויר RTL (הפוך) — פותרים בהיפוך ידני של תווי הטוקן.
//  2. סוגריים "( )" אינם ממוראים אוטומטית ב-RTL — פותרים במירור (↔).
// שני התיקונים נפרדים (מירור על כל הטקסט, היפוך רק על טוקני LTR) — הוכח בבדיקה שהשילוב
// נותן תצוגה נכונה, כולל "(600 ש"ח)". חשוב: מספר חייב הקשר עברי באותה מחרוזת (rightText
// אחד); שדה מספרי המצויר לבדו יש לעטוף ב-RLM כדי לספק הקשר RTL.
// (LRO/POP לא כובדו ע"י הרנדרר; getReorderedString של bidi-js הפך גם את העברית.)

const LTR_TOKEN = /[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}|\d[\d.,:/]*(?:\s*[-–]\s*\d[\d.,:/]*)*/g

export function toVisual(text: string): string {
  return String(text ?? '')
    // 1. מירור סוגריים — ב-RTL הפותח והסוגר מתחלפים ויזואלית
    .replace(/[()]/g, c => (c === '(' ? ')' : '('))
    // 2. היפוך תווי טוקני LTR (מספרים/מייל/שעות)
    .replace(LTR_TOKEN, m => [...m].reverse().join(''))
}
